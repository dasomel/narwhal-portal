export type TranslationKey = keyof typeof ko

const dictionaries: Record<Locale, Record<TranslationKey, string>> = { ko, en }

export function t(locale: Locale, key: TranslationKey, params?: Record<string, string | number>): string {
  const text = dictionaries[locale][key] ?? dictionaries[defaultLocale][key] ?? key
  if (!params) return text
  return text.replace(/\{(\w+)\}/g, (match, k) => (k in params ? String(params[k]) : match))
}

export function getLocaleFromCookie(cookieValue: string | undefined): Locale {
  if (cookieValue === "en" || cookieValue === "ko") return cookieValue
  return defaultLocale
}
